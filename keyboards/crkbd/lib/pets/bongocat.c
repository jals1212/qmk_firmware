static const uint8_t * const idle_frames_rle[IDLE_FRAMES] = {
    idle_rle_0,
    idle_rle_1,
    idle_rle_2,
};

static const uint8_t * const tap_frames_rle[TAP_FRAMES] = {
    tap_rle_0,
    tap_rle_1,
};

static char frame_buf[ANIM_SIZE];

static void rle_decompress(const uint8_t *src, char *dst) {
    uint16_t di = 0;
    while (di < ANIM_SIZE) {
        uint8_t h     = pgm_read_byte(src++);
        uint8_t count = (h & 0x7F) + 1;
        if (h & 0x80) {
            memset(dst + di, (char)pgm_read_byte(src++), count);
        } else {
            memcpy_P(dst + di, src, count);
            src += count;
        }
        di += count;
    }
}

static uint32_t anim_timer = 0;
static uint8_t current_idle_frame = 0;
static uint8_t current_tap_frame = 0;

void render_bongo_cat(void) {
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_timer = timer_read32();

        const uint8_t *next;
        if (get_current_wpm() < IDLE_SPEED) {
            current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
            next = idle_frames_rle[current_idle_frame];
        } else {
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            next = tap_frames_rle[current_tap_frame];
        }
        rle_decompress(next, frame_buf);
    }

    // Write on every call so layer changes that call oled_clear() see the
    // current frame immediately instead of a blank screen until the next tick.
    oled_write_raw(frame_buf, ANIM_SIZE);
}
